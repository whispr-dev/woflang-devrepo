; mth.asm

MTH_ADD:
    ADD R14, R14, R15
    JMP MTH_LOOP

MTH_SUBTRACT:
    SUB R14, R14, R15
    JMP MTH_LOOP

MTH_MULTIPLY:
    MUL R14, R14, R15
    JMP MTH_LOOP

MTH_DIVIDE:
    CMP R15, 0
    JEQ CALL ERR_DIVISION_BY_ZERO
    DIV R14, R14, R15
    JMP MTH_LOOP

MTH_LOOP:
    MOV MEM[200], R14  ; Store result
    RET

